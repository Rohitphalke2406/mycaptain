#include<iostream>
using namespace std;

int main()
{
    int a;
    a =12;

    cout<<"Size of int = "<<sizeof(a)<<endl;

    float b;
    cout<<"Size of float = "<<sizeof(b)<<endl;

    char c;
    cout<<"Size of char = "<<sizeof(c)<<endl;

    double d;
    cout<<"Size of double = "<<sizeof(d)<<endl;

    return 0;
}
